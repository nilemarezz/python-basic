grade = 1000
if(grade <= 50):
    print('F')
elif(grade> 50 and grade <= 60):
    print('D')
elif(grade > 60 and grade <= 70):
    print('C')
elif(grade > 70 and grade <= 80):
    print('B')
elif(grade > 80 and grade <= 100):
    print('A')
else:
    print('Invalid Input')


